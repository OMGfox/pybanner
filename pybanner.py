def prepare_user_string(user_string: str, spacing=2) -> str:
    user_string = user_string.upper()
    prepared_user_string = ""
    char_buffer = list()
    for char in user_string:
        if char in CHARS:
            char_buffer.append(CHARS[char])
        else:
            char_buffer.append(CHARS["*"])
    if len(char_buffer) > 0:
        for line_number in range(len(char_buffer[0])):
            for char in char_buffer:
                prepared_user_string += char[line_number] + " " * spacing
            prepared_user_string += "\n"

    return prepared_user_string


def print_banner(prepared_user_string: str) -> None:
    print(prepared_user_string)


if __name__ == '__main__':
    user_string = argv[1]
    prepared_user_string = prepare_user_string(user_string)
    print_banner(prepared_user_string)
